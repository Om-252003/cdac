# initialize one-d and 2d array  with one  

import numpy as np
first=np.ones((3,3))                             # one row , 4 columns

print(first)
print("\n")
'''[[1. 1. 1.]
 [1. 1. 1.]
 [1. 1. 1.]]'''

# initialize two-d array with zeros

second=np.ones((3,3), int)                            # 3 rows and 4 columns

print(second)
'''[[1 1 1]
 [1 1 1]
 [1 1 1]]'''